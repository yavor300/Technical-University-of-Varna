package bg.tu_varna.sit.b1.f21621577.constants;

/**
 * This class defines status codes that can be used to represent the outcome of an operation.
 * <p>
 * Класът дефинира статусни кодове, които могат да се използват за представяне на резултата от определена операция.
 */
public class StatusCodes {

  /**
   * The status code indicating a successful operation.
   * <p>
   * Статусен код, който указва успешно изпълнение на операцията.
   */
  public static final int SUCCESSFUL_STATUS_CODE = 0;

  /**
   * The status code indicating an unsuccessful operation.
   * <p>
   * Статусен код, който указва неуспешно изпълнение на операцията.
   */
  public static final int UNSUCCESSFUL_STATUS_CODE = 1;
}
